-- ==================================================
-- 1. 新建危险作业申报表 biz_dangerous_work
-- ==================================================
CREATE TABLE IF NOT EXISTS biz_dangerous_work (
    id                  BIGSERIAL PRIMARY KEY,
    work_code           VARCHAR(50)  NOT NULL UNIQUE,        -- 申报编号（自动生成 DW-YYYYMMDD-XXX）
    district_project_id BIGINT REFERENCES biz_project(id),  -- 关联片区项目ID（可为空）
    district_name       VARCHAR(100),                        -- 所属片区名称
    work_type           VARCHAR(50)  NOT NULL,               -- 作业类型
    work_name           VARCHAR(200) NOT NULL,               -- 作业名称
    risk_level          VARCHAR(20)  NOT NULL DEFAULT '一般风险', -- 风险等级
    applicant           VARCHAR(50),                         -- 申请人
    responsible_person  VARCHAR(50),                         -- 负责人
    implementor         VARCHAR(200),                        -- 项目实施主体
    location_desc       VARCHAR(200),                        -- 位置描述
    lng                 DOUBLE PRECISION NOT NULL,           -- 经度
    lat                 DOUBLE PRECISION NOT NULL,           -- 纬度
    plan_start_time     TIMESTAMP,                           -- 计划开始时间
    plan_end_time       TIMESTAMP,                           -- 计划结束时间
    safety_measures     TEXT,                                -- 安全措施（逗号分隔）
    work_description    TEXT,                                -- 作业描述
    status              VARCHAR(20) NOT NULL DEFAULT 'IN_PROGRESS', -- IN_PROGRESS/COMPLETED/CANCELLED
    monitor_code        VARCHAR(50),                         -- 监控点编号（自动生成 MONITOR_XXX）
    created_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    deleted             BOOLEAN DEFAULT FALSE
);

COMMENT ON TABLE  biz_dangerous_work IS '危险作业申报表';
COMMENT ON COLUMN biz_dangerous_work.work_type     IS '高空作业/受限空间作业/危化品储存/起重作业/明火作业';
COMMENT ON COLUMN biz_dangerous_work.risk_level    IS '重大风险/较大风险/一般风险';
COMMENT ON COLUMN biz_dangerous_work.status        IS 'IN_PROGRESS进行中 / COMPLETED已完成 / CANCELLED已取消';
COMMENT ON COLUMN biz_dangerous_work.monitor_code  IS '提交后自动生成的地图监控点编号';

-- 索引
CREATE INDEX IF NOT EXISTS idx_dangerous_work_status ON biz_dangerous_work(status);
CREATE INDEX IF NOT EXISTS idx_dangerous_work_type   ON biz_dangerous_work(work_type);
CREATE INDEX IF NOT EXISTS idx_dangerous_work_date   ON biz_dangerous_work(created_at);

